package pack;

public class LoopsExample {

    //for  -- nested for loop
    //while
    //do while


    public static void main(String[] args) {
        for (int i=0; i<args.length;i++)
        {
            System.out.println("i value "+i);

        }

        //while loop
        int i = 0;
        while(i< args.length)
        {
            System.out.println("i value "+i);
            i++;

        }








    }
}
