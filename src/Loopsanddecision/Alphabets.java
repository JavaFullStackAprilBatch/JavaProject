package Loopsanddecision;

public class Alphabets {
	public static void main(String a[])
	{
		char ch;
		
		System.out.println("UpercasecaseAlphabets :");
		for(ch='A';ch<='Z';ch++)
		{
			System.out.println(ch + " ");
		}
		System.out.println("lowercaseAlphabets :");
		for(ch='a';ch<='z';ch++)
		{
			System.out.println(ch + " ");
		}
		
			
	}

}
